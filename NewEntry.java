    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        // TODO add your handling code here:
        if (evt.getSource() == submitButton) {

            /// Confirm addition of information to database
            int choice = JOptionPane.showConfirmDialog(jframe,"Add this information to the database?","Persons",JOptionPane.YES_NO_OPTION);

            // If YES
            if(choice == JOptionPane.YES_OPTION) {

                try {

                    // load database driver class
                    Class.forName( "sun.jdbc.odbc.JdbcOdbcDriver");

                    // connect to database
                    java.sql.Connection connection = DriverManager.getConnection("jdbc:odbc:personnel" );

                    // create Statement to query database
                    java.sql.Statement statement = connection.createStatement();

                    // Add new acount to Master table
                    java.sql.Statement stmt = connection.createStatement();
                    stmt.executeUpdate(
                            "INSERT INTO persons(first_name,last_name,middle_name,nickname,gender,maritalstatus,age,address,res_phone_no,off_phone_no,cell_number,email_address,company_name,dept_name)VALUES "+"('"

                            +"','"+firstnameText.getText()
                            +"','"+lastnameText.getText()
                            +"','"+middlenameText.getText()
                            +"','"+nicknameText.getText()
                            +"','"+genderText.getText()
                            +"','"+maritalstatusText.getText()
                            +"','"+ageText.getText()
                            +"','"+addressText.getText()
                            +"','"+res_phone_noText.getText()
                            +"','"+off_phone_noText.getText()
                            +"','"+cell_numberText.getText()
                            +"','"+email_addressText.getText()
                            +"','"+company_nameText.getText()
                            +"','"+dptnameText.getText()
                            +"')");

                    JOptionPane.showMessageDialog(null,"Data Entered into Database Successfully");
                            firstnameText.setText("");
                            lastnameText.setText("");
                            middlenameText.setText("");
                            nicknameText.setText("");
                            genderText.setText("");
                            maritalstatusText.setText("");
                            ageText.setText("");
                            addressText.setText("");
                            res_phone_noText.setText("");
                            off_phone_noText.setText("");
                            cell_numberText.setText("");
                            email_addressText.setText("");
                            company_nameText.setText("");
                            dptnameText.setText("");
      


                    statement.close(); // Close statement
                    connection.close(); // Close connection

                    setVisible(false); // Hide frame

                }  // end try

                // detect problems interacting with the database
                catch ( java.sql.SQLException sqlException ) {
                    JOptionPane.showMessageDialog( null,
                            sqlException.getMessage(), "Database Error",
                            JOptionPane.ERROR_MESSAGE );

                    System.exit( 1 );
                }


                catch ( ClassNotFoundException classNotFound ) {
                    JOptionPane.showMessageDialog( null,
                            classNotFound.getMessage(), "Driver Not Found",
                            JOptionPane.ERROR_MESSAGE );

                    System.exit( 1 );
                }



            }
            // If NO
            else{
                            firstnameText.setText("");
                            lastnameText.setText("");
                            middlenameText.setText("");
                            nicknameText.setText("");
                            genderText.setText("");
                            maritalstatusText.setText("");
                            ageText.setText("");
                            addressText.setText("");
                            res_phone_noText.setText("");
                            off_phone_noText.setText("");
                            cell_numberText.setText("");
                            email_addressText.setText("");
                            company_nameText.setText("");
                            dptnameText.setText("");

                            
                setVisible(false); // Hide Frame

            }
        }
}//GEN-LAST:event_submitButtonActionPerformed

